    public void file2List(File file){
//        System.out.println(file.getPath());
//        System.out.println(file.getAbsolutePath());
        //JOptionPane.showMessageDialog(null, file.getAbsolutePath());

        try {
            listaTexto = FileUtils.readLines(file);
        } catch (IOException ex) {
            listaTexto = null;
            System.out.println(ex.getMessage());
            System.out.println("Fallo al cargar el Archivo");
        }
    }
    
    public static void vaciarTabla(DefaultTableModel tabla){
        for (int c = tabla.getRowCount() - 1; c >= 0; c--) {
            tabla.removeRow(c);
        }
    }
    
    public void llenarTablaCoordenadas(DefaultTableModel tabla){
        vaciarTabla(tabla);
        for (int c = 0; c < robot.getCoordenadasConvertidas().size(); c++){
            tabla.addRow(new Object[]{
                robot.getCoordenadasConvertidas().get(c)[0],
                robot.getCoordenadasConvertidas().get(c)[1],
                robot.getListaDeMovimientos().get(c).toString()});
        }
    }

