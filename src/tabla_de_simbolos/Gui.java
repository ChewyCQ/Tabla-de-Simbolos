    private void actualizarTabla(){
        int n = 0;
        for (Nodo nodo : tablaSimbolos.getCasillas()){
            if (nodo != null){
                tabla.setValueAt(nodo.getDato(), n, 0);
                tabla.setValueAt(tablaSimbolos.contarColisionesCasilla(nodo) + 1, n, 3);
            }
            n++;
        }
    }

